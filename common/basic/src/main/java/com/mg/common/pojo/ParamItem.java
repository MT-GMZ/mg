package com.mg.common.pojo;

public class ParamItem {

    private String key;

    private String value;

    private Class clType;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public Class getClType() {
        return clType;
    }

    public void setClType(Class clType) {
        this.clType = clType;
    }
}
